package Day_5;

public class RelationalLogicalOperators {
public static void main(String[] args) {
		
		int i = 200;
		
		System.out.println(i==200);
		System.out.println(i!=200);
		System.out.println(i>=200);
		System.out.println(i<=200);
		
		System.out.println(i==200 && i!=200);
		System.out.println(i==200 || i!=200);
	}

}
